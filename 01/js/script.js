// SOMMA DI NUMERI CON 5 PROMPT FOR LOOP
var somma = 0;
for (var i = 0; i < 5; i++) {
  var numero = parseInt(prompt("inserisci numero"));
  somma += numero;
}
console.log(somma);



// SOMMA DI NUMERI CON 5 PROMPT WHILE
var somma = 0;
var i = 0;
while (i < 5) {
  var numero = parseInt(prompt("inserisci numero"));
  somma += numero;
  i++;
}
console.log(somma);













